      DATA  BETA(1,2)  /0.0D0/ ,  BETA(2,2)  /0.0D0/
      DATA  ALPHA(1,3) /1.5D0/ ,  BETA(1,3) /-0.6D0/
      DATA IW /2/ , JT /1/
C
      WRITE (*,2000) N
      DO 100 I = 0,N,1
         WRITE (*,2100) I,X(I),Y(I),WX(I),WY(I)
  100 CONTINUE
      DO 10 IAB=1,3
        CALL CFSPPA (N,X,Y,WX,WY,T,JT,IAB,ALPHA(1,IAB),BETA(1,IAB),IW,
     +               AX,BX,CX,DX,AY,BY,CY,DY,HILF,IFEHL)
        IF (IFEHL .NE. 0) THEN
           WRITE (*,1100) IFEHL
           STOP
        ENDIF
        WRITE (*,2200)
        IF (IAB .LE. 2) THEN
           WRITE (*,2250) IAB,ALPHA(1,IAB),BETA(1,IAB),ALPHA(2,IAB),
     +                      BETA(2,IAB)
        ELSE
           WRITE (*,2260) ALPHA(1,IAB),BETA(1,IAB)
        ENDIF
        WRITE (*,2270)
        DO 150 I = 0,N,1
           WRITE (*,2275) I,T(I)
  150   CONTINUE
        WRITE (*,2280)
        DO 200 I = 0,N-1,1
           WRITE (*,2300) I,AX(I),BX(I),CX(I),DX(I)
  200   CONTINUE
        WRITE (*,2290)
        DO 250 I = 0,N-1,1
           WRITE (*,2300) I,AY(I),BY(I),CY(I),DY(I)
  250   CONTINUE
C
        WRITE (*,1150) T(0),T(N)
        TANF = T(0)
        TEND = T(N)
        CALL PSPTAB (N,MT,TANF,TEND,T,AX,BX,CX,DX,AY,BY,CY,DY,
     +               NT,XW,YW,IFEHL)
        IF (IFEHL .NE. 0) THEN
           WRITE (*,1400)
           STOP
        ENDIF
        WRITE (*,2400)
        DO 300 I = 0,NT,1
           WRITE (*,2500) I,XW(I),YW(I)
  300   CONTINUE
        WRITE (*,2600)
   10 CONTINUE
      STOP
