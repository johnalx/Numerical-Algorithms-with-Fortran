C[            0           1           2                                  ]*
C[ ----------------------------------------------------------------------]*
C[    0   9.975D-01   9.918D-03  -8.381D-01                              ]*
C[    1   9.975D-01   9.918D-03  -8.381D-01                              ]*
C[    2   9.975D-01   9.918D-03  -8.381D-01                              ]*
C[    3   9.975D-01   9.918D-03  -8.381D-01                              ]*
C[                                                                       ]*
C[ COEFFICIENTS OF (X-X(I)) **  2 * (Y-Y(J)) **  2  :  A(I,J,  2,  2) =  ]*
C[ ----------------------------------------------------------------------]*
C[            0           1           2                                  ]*
C[ ----------------------------------------------------------------------]*
