C[ EXAMPLE FOR USE OF BSPVAL:                                            ]*
C[ ==========================                                            ]*
C[                                                                       ]*
C[ TABLE OF VALUES FOR S(X,Y) AT THE CENTERS (X,Y) OF THE RECTANGLES     ]*
C[ FOR THE MESH, I. E.,                                                  ]*
C[     X = ( X(I)+X(I-1) )/2 , I = 1(1)N                                 ]*
C[     Y = ( Y(K)+Y(K-1) )/2 , K = 1(1)M                                 ]*
C[            1           2           3                                  ]*
C[ ----------------------------------------------------------------------]*
C[    1   1.801D-01   2.190D-01   7.887D-02                              ]*
C[    2   1.621D+00   1.971D+00   7.098D-01                              ]*
C[    3   5.253D+00   6.387D+00   2.300D+00                              ]*
C[    4   1.271D+01   1.546D+01   5.565D+00                              ]*
C                                                                         *
C**************************************************************************
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      PARAMETER(N=4,M=3,KDIM=3,LDIM=3)
C
      DIMENSION A(0:N,0:M,0:KDIM,0:LDIM),X(0:N),Y(0:M)
      DIMENSION F(50),XMIT(N),YMIT(N),ZMIT(N,M)
C
C     Variables for format statements
C
      CHARACTER FO1555*50, FO1560*50
C
C initialize mesh
C
      DATA (X(I),I=0,N)  /0.0D0,1.0D0,2.0D0,3.4D0,5.0D0/
      DATA (Y(I),I=0,M)  /-1.5D0,0.0D0,1.0D0,1.5D0/

C
C compute function on the mesh
C
      DO 10 I=0,N
         DO 10 K=0,M
            A(I,K,0,0) = S(X(I),Y(K))
10    CONTINUE
C
C initialize partial derivative DS/DX
C
      DO 20 K=0,M
         A(0,K,1,0)=DSDX(X(0),Y(K))
         A(N,K,1,0)=DSDX(X(N),Y(K))
20    CONTINUE
C
C initialize partial derivative DS/DY
C
      DO 30 K=0,N
         A(K,0,0,1)=DSDY(X(K),Y(0))
         A(K,M,0,1)=DSDY(X(K),Y(M))
30    CONTINUE
C
C initialize partial derivative D2S/DXDY
C
      A(0,0,1,1)=D2SXY(X(0),Y(0))
      A(N,0,1,1)=D2SXY(X(N),Y(0))
      A(0,M,1,1)=D2SXY(X(0),Y(M))
      A(N,M,1,1)=D2SXY(X(N),Y(M))
C
      WRITE(*,1520)
      WRITE(*,1530) (I,X(I),I=0,N)
      WRITE(*,1540) (I,Y(I),I=0,M)
      WRITE(*,1550)
      WRITE (FO1555, 1555) M + 1
      WRITE(*,FO1555) (I,I=0,M)
      WRITE(*,1556)
      DO 40 I=0,N
         WRITE (FO1560, 1560) M + 1
         WRITE(*,FO1560) I,(A(I,K,0,0),K=0,M)
40    CONTINUE
      WRITE(*,1570)
      WRITE (FO1555, 1555) M + 1
      WRITE(*,FO1555) (I,I=0,M)
      WRITE(*,1556)
      WRITE (FO1560, 1560) M + 1
      WRITE(*,FO1560) 0,(A(0,K,1,0),K=0,M)
      WRITE(*,FO1560) N,(A(N,K,1,0),K=0,M)
      WRITE(*,1580)
      WRITE (FO1555, 1555) N + 1
      WRITE(*,FO1555) (I,I=0,N)
      WRITE(*,1556)
      WRITE (FO1560, 1560) N + 1
      WRITE(*,FO1560) 0,(A(I,0,0,1),I=0,N)
      WRITE(*,FO1560) M,(A(I,M,0,1),I=0,N)
      WRITE(*,1590) A(0,0,1,1),A(N,0,1,1),A(0,M,1,1),A(N,M,1,1)
C
      CALL BICSP1(N,M,A,X,Y,F,IFEHL)
C
      IF(IFEHL.NE.0) THEN
        WRITE(*,1500)
        STOP
      ENDIF
C
      WRITE(*,1595)
      DO 50 IX=0,KDIM
         DO 60 IY=0,LDIM
            IF(IX.EQ.0.AND.IY.EQ.0) GOTO 60
            WRITE(*,1600) IX,IY,IX,IY
            WRITE (FO1555, 1555) M
            WRITE(*,FO1555) (I,I=0,M-1)
            WRITE(*,1556)
            DO 70 I=0,N-1
               WRITE (FO1560, 1560) M
               WRITE(*,FO1560) I,(A(I,K,IX,IY),K=0,M-1)
70          CONTINUE
60       CONTINUE
50    CONTINUE
C
C
C     Spline coefficients have been found;
C     next we apply BSPVAL: 
C     ------------------------------------
C
C
      DO 80 I=1,N
         XMIT(I)=0.5D0*(X(I)+X(I-1))
80    CONTINUE
      DO 90 K=1,M
            YMIT(K)=0.5D0*(Y(K)+Y(K-1))
90    CONTINUE
      DO 100 I=1,N
         DO 100 K=1,M
            CALL BSPVAL(N,M,A,X,Y,XMIT(I),YMIT(K),ZMIT(I,K),J)
            IF(J.NE.0) THEN
              WRITE(*,1510)
              STOP
            ENDIF
100   CONTINUE
      WRITE(*,1610)
      WRITE (FO1555, 1555) M
      WRITE(*,FO1555) (I,I=1,M)
      WRITE(*,1556)
      DO 110 I=1,N
            WRITE (FO1560, 1560) M
            WRITE(*,FO1560) I,(ZMIT(I,K),K=1,M)
110   CONTINUE
      STOP
