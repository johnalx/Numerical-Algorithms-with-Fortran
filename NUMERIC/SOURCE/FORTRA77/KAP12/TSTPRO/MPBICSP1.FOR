C[            0           1           2                                  ]*
C[ ----------------------------------------------------------------------]*
C[    0  -2.220D-16   1.214D-16  -2.220D-16                              ]*
C[    1  -4.441D-16  -3.816D-16   1.332D-15                              ]*
C[    2  -7.117D-16  -1.702D-16  -2.368D-16                              ]*
C[    3  -2.776D-17  -8.795D-16   2.942D-15                              ]*
C[                                                                       ]*
C[ COEFFICIENTS OF (X-X(I)) **  3 * (Y-Y(J)) **  2  :  A(I,J,  3,  2) =  ]*
C[ ----------------------------------------------------------------------]*
C[            0           1           2                                  ]*
C[ ----------------------------------------------------------------------]*
C[    0   3.608D-16   2.220D-16  -4.441D-16                              ]*
C[    1   5.551D-17   4.441D-16   1.332D-15                              ]*
C[    2  -3.358D-17  -4.611D-15   3.372D-15                              ]*
C[    3   1.037D-15   3.303D-15   3.386D-15                              ]*
C[                                                                       ]*
C[ COEFFICIENTS OF (X-X(I)) **  3 * (Y-Y(J)) **  3  :  A(I,J,  3,  3) =  ]*
C[ ----------------------------------------------------------------------]*
C[            0           1           2                                  ]*
C[ ----------------------------------------------------------------------]*
C[    0  -5.551D-17  -2.220D-16  -8.882D-16                              ]*
C[    1   0.000D+00   2.220D-16  -4.441D-15                              ]*
C[    2  -3.388D-18   4.964D-16  -7.663D-16                              ]*
