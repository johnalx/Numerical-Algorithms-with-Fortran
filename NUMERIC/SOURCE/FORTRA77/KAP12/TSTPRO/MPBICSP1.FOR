      DOUBLE PRECISION FUNCTION S (X,Y)
C
C*****************************************************************
C                                                                *
C  compute functional values
C                                                                *
C*****************************************************************
C
      DOUBLE PRECISION X,Y
      S = X*X*DCOS(Y)
      RETURN
      END
C
      DOUBLE PRECISION FUNCTION DSDX (X,Y)
C
C*****************************************************************
C                                                                *
C  compute DS/DX 
C                                                                *
C*****************************************************************
C
      DOUBLE PRECISION X,Y
      DSDX = 2.0D+00*X*DCOS(Y)
      RETURN
      END
C
      DOUBLE PRECISION FUNCTION DSDY (X,Y)
C
C*****************************************************************
C                                                                *
C  compute DS/DY 
C                                                                *
C*****************************************************************
C
      DOUBLE PRECISION X,Y
      DSDY = X*X*(-DSIN(Y))
      RETURN
      END
C
      DOUBLE PRECISION FUNCTION D2SXY (X,Y)
C
C*****************************************************************
C                                                                *
C  compute D2S/DXDY 
C                                                                *
C*****************************************************************
C
      DOUBLE PRECISION X,Y
      D2SXY = 2.D0*X*(-DSIN(Y))
      RETURN
      END
