         - SIN (7.0D0 * X (J) ) - FUNKRE (J) ), DABS ( - 2.0D0 * SIN (  &
         3.0D0 * X (J) ) + COS (7.0D0 * X (J) ) - FUNKIM (J) )          
   50 END DO 
      STOP 
