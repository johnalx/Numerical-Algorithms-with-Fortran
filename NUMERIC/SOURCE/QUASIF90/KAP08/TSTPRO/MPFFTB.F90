         WRITE ( *, 940) K, FUNKRE (K + M), FUNKIM (K + M) 
   20 END DO 
      DO 30 K = 0, (M - 1) / 2 
         WRITE ( *, 940) K, FUNKRE (K), FUNKIM (K) 
   30 END DO 
!                                                                       
! Inverse Fourier transformation                                        
!                                                                       
